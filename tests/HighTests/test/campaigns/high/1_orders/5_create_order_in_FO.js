  test('should open the browser', () => client.open());
  test('should sign in BO', () => client.signinFO());
  test('should change the FO language to english', () => client.changeFOLanguage());
  test('should choose product ', () => client.chooseProduct());
  test('should select product size M ', () => client.selectProductSize('2'));
  test('should select product color blue', () => client.chooseColor());
  test('should add th product quantity', () => client.addQuantity('4'));
  test('should click on add to cart button  ', () => client.clickOnAddToCartButton());
  test('should check the green confirmation is visible', () => client.checkGreenConfirmation());
  test('should click on proceed to checkout button', () => client.clickOnCommandButton());
  test('should click on confirm adress button', () => client.clickOnConfirmAddressesButton());
  test('should choose shipping method my carrier', () => client.chooseShippingMethod());
  test('should create message', () => client.createMessage('Order message test'));
  test('should click on confirm delivery button', () => client.clickOnConfirmDeliveryButton());
  test('should choose payment by bank wire', () => client.choosePayment());
  test('should check the condition to approve', () => client.checkConditionToApprove());
  test('should click on order with an obligation to pay button', () => client.clickOnOrderConfirmationButton());
